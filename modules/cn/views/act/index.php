<!--  ACT备考建议-->
  <div class="a-advice">
    <div class="act-title">
      <h2>ACT备考建议</h2>
      <h4>ACT  Advices</h4>
    </div>
    <ul>
      <li>
        <div class="a-adv-img">
          <div></div>
          <span>ACT英语</span>
        </div>
        <p>ACT英语备考的关键点在于词汇量、阅读能力和做题思维习惯。词汇和阅读能力基础不强的话，容易
          导致备考缓慢，且没效果。ACT英语不仅考查语法能力，还有选词，把握文章重点，写作策略等，要
          把题目和知识点对应起来。除了知识体系，做题思维习惯很大程度上决定了能否突破高分瓶颈。</p>
      </li>
      <li>
        <div class="a-adv-img">
          <div></div>
          <span>ACT数学</span>
        </div>
        <p>普遍而言，ACT数学方面容易丢分有两个原因：疲劳和体感理解难度递增。对简单的题目，也要认真
          对待，与此同时，要加强ACT数学的强度练习，提高不同知识点的灵活运用，对不同题型的敏感度，
          尽量不要犯低级错误，导致丢分，一定要重视数学，定时练习和模考。</p>
      </li>
      <li>
        <div class="a-adv-img">
          <div></div>
          <span>ACT阅读</span>
        </div>
        <p>ACT阅读有两重难点，定位信息和在考试时间内定位信息，需要考生一是增大阅读强度，二是任务型
          阅读。很多时候读文章并不是因为“读”得慢，而是理解慢，能不能快速理解句子，就要求增加阅读
          强度，读更难更复杂的文本。至于任务型阅读，阅读之前知道自己要读的是什么，先读题再读文章，
          有目的性的阅读，可以节省二次阅读的时间。</p>
      </li>
      <li>
        <div class="a-adv-img">
          <div></div>
          <span>ACT科学</span>
        </div>
        <p>科学单项复习要养成良好的阅读题目的习惯。看到图表时下意识问自己一些问题，提高对科学题目的
          敏感度，比如，这组数据有什么具体趋势？两组数据之间明显的对应关系是什么？两个观点对于某个
          特定的内容各自的解释有什么区别？同时，在科学单项，限时刷题是必不可少的，加强做题时间意识、
          对图表和科学语言熟悉，快速把图表的言外之意识别出来。</p>
      </li>
      <li>
        <div class="a-adv-img">
          <div></div>
          <span>ACT写作</span>
        </div>
        <p>写作是高中生的标准化考试中要求很高，不但要写出一篇便于理解的文章，更要在文章中拿出对于问
          题的独特见解，需要考生的判断性思维能力强。大量阅读和对问题的反复思考可以提高考生的判断性
          思维能力。</p>
      </li>
    </ul>
