                        "The Glass Ceiling" is a metaphor that refers to the imperceptible and subversive forms of discrimination women in the workforce encounter when pursuing upper-level management positions. A popular phrase in the 1980s, it has become less widely used over the ensuing years. [Q1] 0ther phrases from past decades are still in frequent use. However, as recent data makes clear, "The Glass Ceiling" remains a solid barrier to women working in [Q2] America!
                        According to data from the U.S. Bureau of Labor Statistics and the Pew Research Center, women make up 57.2% of the nation's workforce [Q3] but hold 5 percent only of CEO positions in Fortune 500 companies. Women also account for only 17% of board membership at Fortune 500 companies. While these percentages note a minimal increase in female leadership over the [Q4] passed thirty years, barriers still seem to exist.
                        [Q5] In 1995, a bipartisan Federal Glass Ceiling Commission was formed; to determine the root of gender discrimination within corporate America. The committee, headed by Secretary of Labor Robert Reich, noted that "At the highest levels of corporations the promise of reward for preparation and pursuit of excellence is not equally available to members of all groups." Two barriers that contributed most to "The Glass Ceiling" [Q6] effect were "supply barriers" and "internal business barriers."
                        "Supply barriers" represent the lack of leadership, education, and experience women are given access to during high school and college. Corporations have viewed this lack of access as an educational reform issue, not a business issue, and [Q7] they do not think it's their responsibility to fix it. However, corporations are equipped with both financial and educational resources to overcome this "supply barrier." If corporations are serious about ending discrimination in the workplace, they need to invest in educational programs that train women to be future leaders. This includes creating more mentoring programs and school-to-work initiatives and providing more educational scholarships. [Q8]
                        [Q9] The difference between what corporate executives say they want to do. In regard to discrimination, and efforts being made (or not made) to end discriminatory practices is another issue. Talking the talk and not walking the walk is what the "internal business barrier" is all about. The reason for this discrepancy is that many white males working in the highly competitive corporate America believe they are "losing the corporate game, losing control, and losing opportunity." In essence, white men in corporate leadership feel threatened by including women in their ranks.
                        [Q10] Ending discrimination against women in the workforce is an involved, complicated process. While many efforts have been made to prevent overt discriminatory practices, 65% of women still see gender bias as a barrier they have to overcome. [Q11] Until women can be as equals accepted in the corporate sphere, "The Glass Ceiling" will remain firmly intact.
