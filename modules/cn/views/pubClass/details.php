                <h2 class="s-pd-title">SAT 2230的重庆学霸,为何一年后被加州大学劝退?</h2>
                <div class="clearfix">
                    <p class="s-pd-time pull-right"><span>2017-5-3</span><span>阅读<i>340</i></span></p>
                </div>
                <h3>摘要：2012年6月，重庆某重点中学的李同学，以2230分的SAT高分、107分的托福成绩被美国加州大学洛杉矶分校经济学院录取。那一年，又一个学霸的故事成为传说。</h3>
                <div class="s-pd-font">2012年6月，重庆某重点中学的李同学，以2230分的SAT高分、107分的托福成绩被美国加州大学洛杉矶分校经济学院录取。那一年，又一个学霸的故事成为传说。

                    SAT 2230的重庆学霸，为何一年后被美国加州大学劝退？图1
                    然而一年后，剧情急转直下，他被加州大学劝退了，一学年9门功课，8门不及格。简直是晴天霹雳，这么快的画风转变让小德差点吓傻。

                    小德脑中浮现出当年备战英语四级的场景，考前一个月，一口气做完10套模拟题，过级妥妥的，虽然我完全没有听懂听力语音，也只读懂了阅读理解大概一半的内容。

                    敢情这出国留学和过四级一样的套路?全真题在手，全世界都敢走?然而......
                    分数很美好，现实很残酷。留学真的就是雅思、托福加SAT高分就可以了吗?
                    通过应试方法刷出的SAT高分，真的能达到世界顶尖大学要求的学术水平吗?高中三年，除了刷题，还学会了些什么，还应该学会些什么?

                    ■出国前的学霸是怎样练成的

                    每天花10小时完成各种SAT模拟试题
                    SAT分为三个部分：分析性阅读、数学、写作，每一部分满分是800分，考查的是学生的逻辑推理能力。
                    为了进名校，李同学每天都会花至少10个小时的时间完成各种SAT模拟试题，加上兼顾复习托福的时间，高二和高三两年，李每晚都只有6个小时的休息时间。
                    背超过200篇范文，重复做过4遍历年真题
                    李说，他用了两年的时间，背了超过200篇范文，重复做过4遍历年SAT真题和模拟题。
                    他终于在2012年3月的SAT考试中考了2230分(满分2400)，再加上托福107分的高分，成功申请到加州大学洛杉矶分校。

                    ■出国后遭遇滑铁卢

                    然而，李在进入这所世界名校之后，才发现，他遇到了一系列学习上的问题：
                    1.做不出论文(论文成绩在期末成绩中占30%)
                    在美国，每学期第一堂课教授都会告诉学生论文的要求，让学生有一个学期的时间去准备，而论文成绩在期末成绩中占30%。
                    但在李的印象中，第一节课，他就发现自己完全听不懂教授的讲述，那些似曾相识却又怎么也想不起意思的单词，从李的耳边飞快地掠过。只会英语笔试的弊端开始显现。
                    李说，美国学生从高中开始就会接触论文写作，但他由于在中学时从未写过论文，所以他连论文方向如何定都不知道。
                    “加上每次上课都听不太懂，题目也没有确定，看着图书馆里的那些资料，我觉得自己简直无从下手。”

                    2.无法融入小组(小组作业在整个考评中占25%)
                    小组作业通常会在学习半学期后展开，这也是西方教育的传统，在学生的整个考评中占25%的成绩。
                    它要求学生自己寻找合作对象，组成一个五人左右的小组，进行半学期的学科研究，最后完成30分钟的学术演讲。
                    而这些程序，初来乍到的李毫不知情。
                    李说，第一节课教授就告知他会有一个小组作业，但如何操作却没有说明，这就导致李认为会像在国内学习时那般，以学号或者由老师指定组合，所以，他也没有去担心寻找小组成员的问题。
                    直到半期后，许多课程教授纷纷要求学生提交小组成员名单时，李才意识到自己落单了。“我去找教授，他们都告诉我这必须自己解决。
                    一个星期后，由于我还是没有找到团队，所以教授们只能给我指派。”李说，他的组员包括两个日本人与两个中东人，至今他都不清楚对方的名字。
                    由于李的性格比较内向，他并没有主动与组员交流，通常开小组会议，他都是最后一个知道，有时甚至不会通知他。

                    3.学期期末演讲困难
                    李说，虽然学校没有要求，但他所在的专业一学期至少要修满3~5门课程才能按时毕业。然而，这些课的期末演讲几乎成了他的留学梦魇。
                    由于内向的性格原因，在国内从未参加过演讲的李，在得知要当着一百多人用英语陈述自己的学术报告时，他甚至有些后悔这么早到美国学习。
                    “为了不出丑，每门演讲我都花了至少一个月的时间准备，把内容背诵过无数次。”李说，他的第一门演讲是管理，当他带着自己的PPT走上讲台，做完自我介绍以后，却忽然忘记了自己的发言内容。
                    “原本精心准备的演讲，讲得乱七八糟。现在想起来，15分钟的演讲，我可能只说了6分钟左右。”
                    李承认自己完全不适应没有老师引导的自主学习方式，在经历过小组演讲、学术演讲和论文的挫折后，他开始反思美国教育模式是否真的适合自己。

                    4.一学年9门考试挂8门被劝退
                    日复一日的过去，李依旧没有能找到自己学习的入门方法。
                    一个星期一的下午，李收到加州大学学生中心的邮件，点开之后他顿感五雷轰顶。
                    “他们告诉我，由于这学年9门考试我挂了8门，学习情况与入学成绩不符，暂停我在加州大学的学习。”
                    至此，李结束了他的美国本科学习。
                    美国常青藤盟校公布的数据显示，进入哈佛大学、耶鲁大学、康奈尔大学、哥伦比亚大学等14所名牌大学的中国留学生退学率为25%，他们的共同特点为——都是曾经的高分考生。
                    这是一个惊人的数字，但媒体的报道凤毛麟角。因为，人们更喜欢听那些亮丽的故事，某某某考上了哈佛、耶鲁，而没有人说某某某被哈、耶遣送回家。
                    所以并不等于说你SAT考了高分，就前程无忧了，拼搏在个人呀！


