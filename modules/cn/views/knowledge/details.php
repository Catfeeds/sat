                    <h2>解释题型及例题分析</h2>
                    <p>&nbsp;</p>
                    <p>增强题，又称strengthen题目。通常需要寻找支持某一个命题的结论或某个人的观点的证据或依据，使之成立可能性提高。</p>
                    <p>&nbsp;</p>
                    <p>标志性词语：strengthen、support、provide the best evidence</p>
                    <p>&nbsp;</p>
                    <p>解题思路：增加结论成立或排除其他原因，使得文章的结论更加严谨。</p>
                    <p>&nbsp;</p>
                    <p>解题步骤：
                    <p>1、加强plan-goal结论：</p>
                    <p>（1）goal能够达到，或能更好地达到（弥补/架桥）提出新观点</p>
                    <p>（2）别的plan不好（重复）否定新观点</p>
                    <p>（3）plan有好处，或无害，或可行（重复）肯定老观点</p>
                    <p>2、加强现象解释文：通常是否定句</p>
                    <p>（1）否命题的例子，例子与结论相一致；</p>
                    <p>（2）对结论的反面的否定</p>
                    <p>（3）如果是已经有了结论的现象解释文，只要重复一下该解释就算加强了</p>
                    <p>3、加强普通文章：</p>
                    <p>（1）问题是argument，加强推理过程，优先考虑弥补/架桥</p>
                    <p>（2）问题是conclusion，加强结论，优先考虑重复</p>
                    <p>&nbsp;</p>
                    <p>解题技巧：
                    <p>1、找出原文中的结论</p>
                    <p>2、锁定结论，几乎所有正确答案都对结论有影响</p>
                    <p>3、原文中有推理漏洞，需要仔细寻找加以支持</p>
                    <p>4、预先判断正确选项特征范围</p>
                    <p>&nbsp;</p>
                    <p>错误答案特征：</p>
                    <p> 1）相反答案（即削弱）</p>
                    <p>2）伪装答案——答案加强了一个与原文结论相似的结论</p>
                    <p>3）无关答案</p>
                </div>
                <div class="s-example">
                    <p>例题：OG16-P502-2</p>
                    <p>&nbsp;</p>
                    <p>Astronomer: Most stars are born in groups of thousands, each star in a group forming from the same parent cloud of gas. Each cloud has a unique, homogeneous chemical composition. Therefore, whenever two stars have the same chemical composition as each other, they must have originated from the same cloud of gas.</p>
                    <p>&nbsp;</p>
                    <p>Which of the following, if true, would most strengthen the astronomer’s argument?</p>
                    <p>&nbsp;</p>
                    <p><span>(A) In some groups of stars, not every star originated from the same parent cloud of gas.</span></p>
                    <p>(B) Clouds of gas of similar or identical chemical composition may be remote from each other.</p>
                    <p>(C) Whenever a star forms, it inherits the chemical composition of its parent cloud of gas.</p>
                    <p>(D) Many stars in vastly different parts of the universe are quite similar in their chemical compositions.</p>
                    <p>&nbsp;</p>
                    <p>解析：题意：星星以组为单位产生；</p>
                    <p>一组星星来自同一个气云；</p>
                    <p>同一个气云的化学成分是独一无二的</p>
                    <p>A 讨论星星的来历，无关</p>
                    <p>B 讨论星星的距离无关</p>
                    <p>C 正确，增强搭桥：既然这两颗星星的成分是一样的，那么说明构成它们的气云的化学成分也是一样的，而每一个气云里的化学成分都是特定的，那么说明这两颗星星是来自同一个气云。</p>
                    <p>D 讨论宇宙中星星的成分相似，无关</p>
                </div>
